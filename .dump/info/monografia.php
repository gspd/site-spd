			<p align="center" class="title"><strong>PROJETO FINAL</strong> </p>
			<p align="justify">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Na maioria das vezes, o aluno tem dificuldades de estruturar uma monografia para apresentar. Alguns tomam como refer&ecirc;ncia a forma de uma apostila ou de um livro. No entanto, cada tipo de documento tem sua pr&oacute;pria forma de ser redigido e apresentado. Para facilitar, daremos a seguir algumas regras para a escrita de disserta&ccedil;&atilde;o de um trabalho cient&iacute;fico. </p>
			<p>&nbsp;</p>
			<p class="subtitle"><strong>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Inicialmente daremos as contra-regras, isto &eacute;, o que n&atilde;o deve ser feito:</strong> </p>
			<p>1 - Afirmar algo sem provar. </p>
			<p>2 - Descrever id&eacute;ias que n&atilde;o s&atilde;o suas sem citar o autor. </p>
			<p>3 - Generalizar algo sem conhecer profundamente todo o assunto (ser autoridade no assunto). </p>
			<p>4 - Concluir algo precipitadamente. </p>
			<p>5 - Criticar algo com autoridade que n&atilde;o tem . </p>
			<p>&nbsp;</p>
			<p class="subtitle"><strong>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Regras b&aacute;sicas para escrever-se uma disserta&ccedil;&atilde;o: </strong></p>
			<p>1 - Utilizar reda&ccedil;&atilde;o t&eacute;cnica, isto &eacute;, n&atilde;o utilizar frases de efeito ou de publicidade, nem linguagem coloquial. </p>
			<p>2 - Legendar todas figuras, tabelas, equa&ccedil;&otilde;es, gr&aacute;ficos,etc </p>
			<p>3 - Destacar os cap&iacute;tulos . </p>
			<p>4 - A conclus&atilde;o &eacute; fundamental, pois sem ela o trabalho n&atilde;o existe cientificamente. </p>
			<p>&nbsp;</p>
			<p class="subtitle"><strong>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Refer&ecirc;ncia para desenvolvimento da monografia</strong></p>
			<p><strong>Cap&iacute;tulo 1 – Introdu&ccedil;&atilde;o</strong></p>
			<p><strong>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;1.1 - Considera&ccedil;&otilde;es iniciais</strong></p>
			<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Apresenta&ccedil;&atilde;o do tema em um escopo mais abrangente, iniciando o leitor no assunto que ser&aacute; tratado. </p>
			<p><strong>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;1.2 - Motiva&ccedil;&atilde;o e escopo </strong></p>
			<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Os aspectos e elementos que fazem do tema algo relevante e que mere&ccedil;a ser tratado pelo trabalho. </p>
			<p><strong>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;1.3 - Objetivos e metodologias </strong></p>
			<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Hist&oacute;rico tecnol&oacute;gico - apresenta&ccedil;&atilde;o breve do hist&oacute;rico tecnol&oacute;gico do tema a ser tratado e metodologia utilizada para o desenvolvimento do trabalho. Neste &uacute;ltimo item, se o trabalho comp&otilde;e-se da implementa&ccedil;&atilde;o de um sistema computacional, deve-se apresentar a metodologia de desenvolvimento utilizada. </p>
			<p><strong>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;1.4 - Organiza&ccedil;&atilde;o da monografia </strong></p>
			<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Apresenta&ccedil;&atilde;o breve dos cap&iacute;tulos seguintes que comp&otilde;em a monografia.</p>
			<p><strong>Cap&iacute;tulo 2 - Revis&atilde;o bibliogr&aacute;fica: </strong></p>
			<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Neste cap&iacute;tulo fazemos o levantamento do que j&aacute; foi realizado na dire&ccedil;&atilde;o do trabalho proposto e dos m&eacute;todos a serem utilizados. <br>
			&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Deve ser escrito na terceira pessoa, pois trata-se de descri&ccedil;&atilde;o de trabalhos realizados por outrem. <br>
			&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Quando estivermos tratando de algo pertinente a um trabalho (artigo), deve-se fazer a cita&ccedil;&atilde;o do artigo e seus autores de forma padronizada. </p>
			<p><strong>Cap&iacute;tulo 3 - O trabalho desenvolvido pelo aluno </strong></p>
			<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Objetivos do trabalho. <br>
			&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Algoritmos (se existirem). <br>
			&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Descri&ccedil;&atilde;o detalhada do problema. <br>
			&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Implementa&ccedil;&atilde;o (descri&ccedil;&atilde;o da implementa&ccedil;&atilde;o, se necess&aacute;rio utilizar figuras e diagramas). </p>
			<p><strong>Cap&iacute;tulo 4 - Testes, resultados e avalia&ccedil;&atilde;o dos resultados </strong></p>
			<p><strong>Cap&iacute;tulo 5 - Conclus&atilde;o: </strong></p>
			<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;O que foi conseguido. <br>
			&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Se foram satisfat&oacute;rios os resultados. <br>
			&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Cr&iacute;ticas. <br>
			&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Sugest&otilde;es para trabalhos futuros. </p>
			<p><strong>Refer&ecirc;ncias bibliogr&aacute;ficas: </strong></p>
			<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Descri&ccedil;&atilde;o completa e seguindo as normas da ABNT das bibliografias citadas nos cap&iacute;tulos anteriores </p>
			<p><strong>Gloss&aacute;rio </strong></p>
			<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Breve descri&ccedil;&atilde;o dos principais termos utilizados na monografia </p>
			<p><strong>Ap&ecirc;ndice </strong></p>
			<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Composto por material desenvolvido pelo autor e que deve completar a compreens&atilde;o do trabalho apresentado. Como exemplo, podemos citar o Manual de opera&ccedil;&atilde;o ou utiliza&ccedil;&atilde;o. </p>
			<p><strong>Anexos:</strong></p>
			<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Composto por material desenvolvido por outro(s) autor(es) e que deve completar a compreens&atilde;o do trabalho apresentado.</p>
			<p class="errmsg"><strong>Observa&ccedil;&otilde;es:</strong></p>
			<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Os cap&iacute;tulos 3, 4 e 5 dever&atilde;o ser escritos na primeira pessoa do plural, pois trata-se do corpo do trabalho propriamente dito, e os autores s&atilde;o: alunos que participaram do trabalho e seus orientadores. </strong></p>
			<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Cada cap&iacute;tulo dever&aacute; ter as figuras (gr&aacute;ficos, equa&ccedil;&otilde;es, diagramas, tabelas, etc) com numera&ccedil;&atilde;o dentro do pr&oacute;prio cap&iacute;tulo. Toda figura (gr&aacute;ficos, equa&ccedil;&otilde;es, diagramas, tabelas, etc) dever&aacute; ter legenda e se tiver sido retirada de alguma obra, dever&aacute; ser referenciada. Os cap&iacute;tulos dever&atilde;o conter a numera&ccedil;&atilde;o das p&aacute;ginas para facilitar as avalia&ccedil;&otilde;es. Os cap&iacute;tulos 2, 3 e 4 devem ser abertos/fechados com as sub-se&ccedil;&otilde;es: </p>
			<p><strong>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Considera&ccedil;&otilde;es iniciais </strong> (abre-se o cap&iacute;tulo descrevendo-se o que ser&aacute; apresentado neste cap&iacute;tulo, n&atilde;o &eacute; necess&aacute;rio escrever como subt&iacute;tulo Considera&ccedil;&otilde;es iniciais). </p>
			<p><strong>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2.x Considera&ccedil;&otilde;es finais </strong> (fecha-se, efetuando-se conclus&otilde;es sobre o que foi descrito neste cap&iacute;tulo).</p>
